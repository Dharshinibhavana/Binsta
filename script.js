document.getElementById("likeBtn1").addEventListener("click", function() {
    let btn = document.getElementById("likeBtn1");
    if (btn.textContent === "Like") {
        btn.textContent = "Liked🖤💓";
        btn.style.color = "red";
    } else {
        btn.textContent = "Like";
        btn.style.color = "#0095f6";
    }
});

